import * as path from 'path';
import { Duration } from 'aws-cdk-lib';
import * as dynamodb from 'aws-cdk-lib/aws-dynamodb';
import * as events from 'aws-cdk-lib/aws-events';
import * as targets from 'aws-cdk-lib/aws-events-targets';
import { Runtime, Architecture } from 'aws-cdk-lib/aws-lambda';
import * as lambda from 'aws-cdk-lib/aws-lambda-nodejs';
import { NagSuppressions } from 'cdk-nag';
import { Construct } from 'constructs';

/**
 * Properties for ConcurrencyReconciler construct.
 */
export interface ConcurrencyReconcilerProps {
  /**
   * The DynamoDB task table (has UserStatusIndex GSI).
   */
  readonly taskTable: dynamodb.ITable;

  /**
   * The DynamoDB user concurrency table.
   */
  readonly userConcurrencyTable: dynamodb.ITable;

  /**
   * How often to run the reconciler.
   * @default Duration.minutes(15)
   */
  readonly schedule?: Duration;
}

/**
 * Scheduled Lambda that reconciles user concurrency counters by comparing
 * the active_count in the concurrency table against actual active tasks
 * in the task table. Corrects drift caused by orchestrator crashes.
 */
export class ConcurrencyReconciler extends Construct {
  public readonly fn: lambda.NodejsFunction;

  constructor(scope: Construct, id: string, props: ConcurrencyReconcilerProps) {
    super(scope, id);

    const handlersDir = path.join(__dirname, '..', 'handlers');

    this.fn = new lambda.NodejsFunction(this, 'ReconcilerFn', {
      entry: path.join(handlersDir, 'reconcile-concurrency.ts'),
      handler: 'handler',
      runtime: Runtime.NODEJS_24_X,
      architecture: Architecture.ARM_64,
      timeout: Duration.minutes(5),
      memorySize: 256,
      environment: {
        TASK_TABLE_NAME: props.taskTable.tableName,
        USER_CONCURRENCY_TABLE_NAME: props.userConcurrencyTable.tableName,
      },
      bundling: {
        externalModules: ['@aws-sdk/*'],
      },
    });

    props.taskTable.grantReadData(this.fn);
    props.userConcurrencyTable.grantReadWriteData(this.fn);

    const schedule = props.schedule ?? Duration.minutes(15);
    const rule = new events.Rule(this, 'ReconcilerSchedule', {
      schedule: events.Schedule.rate(schedule),
    });
    rule.addTarget(new targets.LambdaFunction(this.fn));

    NagSuppressions.addResourceSuppressions(this.fn, [
      {
        id: 'AwsSolutions-IAM4',
        reason: 'AWSLambdaBasicExecutionRole is required for CloudWatch Logs access',
      },
      {
        id: 'AwsSolutions-IAM5',
        reason: 'DynamoDB index/* wildcards generated by CDK grantReadData/grantReadWriteData',
      },
    ], true);
  }
}
